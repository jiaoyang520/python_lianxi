# 十六进制以0x或者0x开头--->> 1~9，A~F 十六进一
print(0x0A) # 10
print(0x07) # 7
# 十进制以0o或者0O开头--->> 0~7  满八进一
print(0o06)     # 6
print(0o012)    # 10
# 二进制以0b或者0B开头--->> 0 1  满二进一
print(0b1010)   # 10

"""
内置函数：
    hex(值)      将10进制转换为16进制
    oct(值)      将10进制的数转成8进制
    bin(值)      将10进制转成2进制
"""
print(hex(10))      # 0xa
print(oct(10))      # 0o12
print(bin(10))      # 0b1010
