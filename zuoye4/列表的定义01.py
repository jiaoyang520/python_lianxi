# 列表相加就是合并
list1 = [1, 2, 3]
list2 = [4, 5, 6]
list3 = list1 + list2
print(list3)

#   列表可以相乘使用*    相乘多少次复制多少次在进行合并
list4 = ["hello", "word!"]
list5 = list4 * 5
print(list5)

#   通过多个变量来来接受元素
list01, list02 = ["hello", "word!"]
list03, list04 = ["xiang", "yang"], ["jiao", "yang"]
print(list01)
print(list02)
print(list03)
print(list04)