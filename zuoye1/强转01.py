# 将int转为bool
print(bool(7))  # 非零--->>True
print(bool(0))  # 0--->>False
# 将bool转为int
print(bool(1))  # 1--->>True
print(bool(0))  # 0--->>False
# 将float转为bool
print(bool(0.007))  # 非零--->>True
print(bool(0.00000))    # 1--->>False
# 将bool转为float
print(float(True)) # True--->>1.0
print(float(False)) # False--->>0.0
# 将int转为float
print(float(7))
# 将float转为float
print(int(7.77777))