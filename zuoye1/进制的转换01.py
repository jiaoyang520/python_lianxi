# 16进制转换为10进制
print(0xF)      # 十六进制以0x或者0X开头
print(0X7)
# 8进制转为10进制
print(0o12)     # 八进制以0o或者0O开头
print(0O07)
# 2进制转为10进制
print(0b1010)      # 二进制以0b或者0B开头
print(0B1000)
# 10进制转为16进制
print(hex(77))
# 10进制转为8进制
print(oct(77))
# 10进制转为2进制
print(bin(77))
# 将16进制的字符串转为10进制数字
print(int("77", 16))
# 将8进制的字符串转为10进制数字
print(int("77", 8))
# 将2进制的字符串转为10进制数字
print(int("101010", 2))
